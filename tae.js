// Filter functionality
const filterButtons = document.querySelectorAll('.filter-btn');
const items = document.querySelectorAll('.item');

filterButtons.forEach(button => {
  button.addEventListener('click', () => {
      // Update active button
      filterButtons.forEach(btn => btn.classList.remove('active'));
      button.classList.add('active');
      
      // Filter items
      const filterValue = button.getAttribute('data-filter');
      
      items.forEach(item => {
          if (filterValue === 'all' || item.getAttribute('data-category') === filterValue) {
              item.classList.remove('hidden');
              // Add slight delay for animation effect
              setTimeout(() => {
                  item.style.opacity = '1';
              }, 50);
          } else {
              item.classList.add('hidden');
              item.style.opacity = '0';
          }
      });
  });
});

// Modal functionality
const modal = document.getElementById('detailModal');
const closeModal = document.querySelector('.close-modal');
const readMoreButtons = document.querySelectorAll('.item-more');

readMoreButtons.forEach(button => {
  button.addEventListener('click', () => {
      const id = button.getAttribute('data-id');
      const data = detailsData[id];
      
      // Populate modal with data
      document.querySelector('.modal-img').src = data.image;
      document.querySelector('.modal-title').textContent = data.title;
      document.querySelector('.modal-subtitle').textContent = data.subtitle;
      document.getElementById('modal-year').textContent = data.year;
      document.getElementById('modal-category').textContent = data.category;
      document.querySelector('.modal-description').innerHTML = `<p>${data.description.replace(/\n\n/g, '</p><p>')}</p>`;
      
      // Show modal
      modal.style.display = 'flex';
      document.body.style.overflow = 'hidden'; // Prevent scrolling when modal is open
      
      // Animation
      modal.style.opacity = '0';
      setTimeout(() => {
          modal.style.opacity = '1';
      }, 10);
  });
});

// Close modal functionality
closeModal.addEventListener('click', () => {
  modal.style.opacity = '0';
  setTimeout(() => {
      modal.style.display = 'none';
      document.body.style.overflow = 'auto'; // Re-enable scrolling
  }, 300);
});

// Close modal when clicking outside content
modal.addEventListener('click', (e) => {
  if (e.target === modal) {
      modal.style.opacity = '0';
      setTimeout(() => {
          modal.style.display = 'none';
          document.body.style.overflow = 'auto';
      }, 300);
  }
});

